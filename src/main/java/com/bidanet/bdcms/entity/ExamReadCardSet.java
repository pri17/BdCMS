@Entity
@Table(name = "exam_readCard_set")
public class ExamReadCardSet {

    private Long id;

    /**
     * 项目名称
     */
    private Integer tag;

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Integer getTag() {
        return tag;
    }

    public void setTag(Integer tag) {
        this.tag = tag;
    }
}
